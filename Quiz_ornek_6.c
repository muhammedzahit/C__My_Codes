#include <stdio.h>
#include <stdlib.h>
#include <string.h>
int asalMi(int a){
    int sayac = 0;
    if (a == 1 || a == 2) return a-1;
    for (int i=2; i<a; i++){
        if (a%i == 0){
            sayac++;
            break;
        }
    }
    return sayac == 0;
}
int main () {

    int kadar;
    printf("Kaca kadar goruntulemek istiyorsunuz ? ");
    scanf("%d",&kadar);

    for (int i=1; i<kadar ; i++){
        char a[200];
        strcpy(a,"");
        char b[20];
        char c[20];
        int e;
        float d;
        if (asalMi(i)){
            strcpy(b,"(Asal)");
            strcpy(c,"^^");
            e = i*i;
            d = e;
        }
        else{
            if (i % 2 == 1){
                strcpy(b,"(Tek )");
                strcpy(c,"//");
                e = i/2;
                d = i/2.00;
            }
            else{
                strcpy(b,"(Cift)");
                strcpy(c,"><");
                e = i*2;
                d = e;
            }
        }
        sprintf(a,"|%3d%6s| ...%s... |%5d|..>>..|%9.4f|",i,b,c,e,d);
        puts(a);
    }

    return 0;
}

